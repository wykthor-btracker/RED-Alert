import type { ReferenceItem } from "@/app/types/reference";
import type { WeaponTypeKey } from "@/data/weaponRangeTables";

export interface ReferenceWeapon extends ReferenceItem {
  /** Key for range table (BASE_SHOT_DV) or "melee". */
  weaponType?: WeaponTypeKey;
  /** Damage dice string e.g. "2d6", "5d6". */
  damageDice?: string;
  /** Skill used e.g. "Armas de uma Mão". */
  skill?: string;
  /** Has autofire (uses AUTOFIRE_DV table). */
  autofire?: boolean;
  /** Magazine size (Carregador Único). */
  magazineSize?: number | "N/A";
  /** Rate of fire per ação (CDT). */
  rof?: number;
  /** Hands needed. */
  hands?: number;
  /** Whether it can be concealed (SIM/NÃO). */
  concealable?: boolean;
  /** Cost tier label, e.g. Oneroso, Premium, Caro. */
  costTier?: "Oneroso" | "Premium" | "Caro";
  /** Special fire modes / notes. */
  special?: string;
  /** Melee only: category for "Tipo de Arma Branca" (Leve, Média, Pesada, Muito Pesada). */
  meleeCategory?: "leve" | "media" | "pesada" | "muito_pesada";
}

